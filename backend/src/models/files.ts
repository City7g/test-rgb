import mongoose from 'mongoose'
const { Schema } = mongoose

const FileSchema = new Schema(
  {
    filename: {
      type: String,
      required: true,
    },
    path: {
      type: String,
      required: true,
    },
    user: {
      type: Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  {
    timestamps: true,
  }
)

const File = mongoose.model('File', FileSchema)

export default File
